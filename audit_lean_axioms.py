ALLOWED_AXIOMS = {"propext", "Classical.choice", "Quot.sound"}
AXIOM_LINE_RE = re.compile(
    r"^'(?P<theorem>[^']+)' depends on axioms: \[(?P<axioms>.*)\]$"
)
NO_AXIOM_LINE_RE = re.compile(
    r"^'(?P<theorem>[^']+)' does not depend on any axioms$"
)


def lake_command() -> str:
    lake = shutil.which("lake")
    if lake is not None:
        return lake

    elan_lake = Path.home() / ".elan" / "bin" / "lake"
    if elan_lake.exists():
        return str(elan_lake)

    raise RuntimeError("lake not found; install Lean with elan or source ~/.elan/env")


def parse_axiom_line(line: str) -> tuple[str, set[str]]:
    no_axiom_match = NO_AXIOM_LINE_RE.match(line.strip())
    if no_axiom_match is not None:
        return no_axiom_match.group("theorem"), set()

    match = AXIOM_LINE_RE.match(line.strip())
    if match is None:
        raise ValueError(f"cannot parse Lean axiom output line: {line!r}")

    raw_axioms = match.group("axioms").strip()
    axioms = set()
    if raw_axioms:
        axioms = {part.strip() for part in raw_axioms.split(",") if part.strip()}
    return match.group("theorem"), axioms


def iter_axiom_output_lines(output: str) -> list[str]:
    axiom_lines: list[str] = []
    pending: str | None = None

    for raw_line in output.splitlines():
        line = raw_line.strip()
        if "does not depend on any axioms" in line:
            if pending is not None:
                axiom_lines.append(pending)
                pending = None
            axiom_lines.append(line)
        elif "depends on axioms:" in line:
            if pending is not None:
                axiom_lines.append(pending)
            pending = line
        elif pending is not None:
            pending = f"{pending} {line}"
        else:
            continue

        if pending is not None and "]" in line:
            axiom_lines.append(pending)
            pending = None

    if pending is not None:
        axiom_lines.append(pending)

    return axiom_lines


def render_lean_probe(theorems: list[str]) -> str:
    lines = [
        "import Gdbh",
        "import Gdbh.VonMangoldtGoldbach",
        "import Gdbh.MathlibExtras.DirichletLFunctions",
        "",
    ]
    lines.extend(f"#print axioms {theorem}" for theorem in theorems)
    return "\n".join(lines) + "\n"


def collect_axioms(theorems: list[str]) -> dict[str, set[str]]:
    with tempfile.TemporaryDirectory() as tmpdir:
        probe_path = Path(tmpdir) / "AxiomAudit.lean"
        probe_path.write_text(render_lean_probe(theorems))
        result = subprocess.run(
            [lake_command(), "env", "lean", str(probe_path)],
            cwd=PROJECT_ROOT,
            text=True,
            capture_output=True,
            check=False,
        )

    if result.returncode != 0:
        raise RuntimeError(result.stderr or result.stdout)

    parsed: dict[str, set[str]] = {}
    for line in iter_axiom_output_lines(result.stdout):
        theorem, axioms = parse_axiom_line(line)
        parsed[theorem] = axioms
    return parsed


def audit_axioms(
    theorems: list[str] = SELECTED_THEOREMS,
    theorem_axioms: dict[str, set[str]] | None = None,
) -> list[str]:
    if theorem_axioms is None:
        theorem_axioms = collect_axioms(theorems)
    failures: list[str] = []

    for theorem in theorems:
        if theorem not in theorem_axioms:
            failures.append(f"{theorem}: missing #print axioms output")
            continue

        unexpected = theorem_axioms[theorem] - ALLOWED_AXIOMS
        if unexpected:
            failures.append(
                f"{theorem}: unexpected axioms {sorted(unexpected)}; "
                f"allowed {sorted(ALLOWED_AXIOMS)}"
            )

    return failures


def main() -> int:
    theorem_axioms = collect_axioms(SELECTED_THEOREMS)
    failures = audit_axioms(SELECTED_THEOREMS, theorem_axioms)
    if failures:
        for failure in failures:
            print(f"FAIL: {failure}")
        return 1

    for theorem in SELECTED_THEOREMS:
        axioms = ", ".join(sorted(theorem_axioms[theorem]))
        print(f"OK: {theorem}: [{axioms}]")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
